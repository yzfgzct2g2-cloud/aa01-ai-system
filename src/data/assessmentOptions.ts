const makeOptions = (
  labels: string[],
  summarySubject: string,
  codes?: string[],
  jumps?: Record<string, string>
): AssessmentOption[] =>
  labels.map((label, index) => {
    const code = codes?.[index] ?? String(index + 1);
    return opt(code, label, `${summarySubject}：${label}。`, jumps?.[code]);
  });

const question = (
  id: string,
  title: string,
  type: AssessmentQuestion["type"],
  options?: AssessmentOption[],
  section?: AssessmentQuestion["section"]
): AssessmentQuestion => ({
  id,
  title,
  type,
  options,
  section: section ?? (id.charAt(0) as AssessmentQuestion["section"]),
});

const gQuestion = (
  id: string,
  title: string,
  type: AssessmentQuestion["type"],
  options?: AssessmentOption[]
) => question(id, title, type, options, "G");

